function numberToGermanWords(n: number): string {
  if (n === 0) return "null";

  const ones = ["", "ein", "zwei", "drei", "vier", "fünf", "sechs", "sieben", "acht", "neun",
    "zehn", "elf", "zwölf", "dreizehn", "vierzehn", "fünfzehn", "sechzehn", "siebzehn", "achtzehn", "neunzehn"];
  const tens = ["", "", "zwanzig", "dreißig", "vierzig", "fünfzig", "sechzig", "siebzig", "achtzig", "neunzig"];

  function belowThousand(num: number): string {
    if (num === 0) return "";
    if (num < 20) return ones[num]!;
    const t = Math.floor(num / 10);
    const o = num % 10;
    if (o === 0) return tens[t]!;
    return ones[o] + "und" + tens[t];
  }

  function belowMillion(num: number): string {
    if (num === 0) return "";
    const h = Math.floor(num / 100);
    const rest = num % 100;
    let result = "";
    if (h > 0) result += ones[h] + "hundert";
    if (rest > 0) result += belowThousand(rest);
    return result;
  }

  let remaining = Math.round(n);
  let result = "";

  if (remaining >= 1000000) {
    const millions = Math.floor(remaining / 1000000);
    result += (millions === 1 ? "eine" : belowMillion(millions)) + " Million" + (millions === 1 ? "" : "en") + " ";
    remaining %= 1000000;
  }

  if (remaining >= 1000) {
    const thousands = Math.floor(remaining / 1000);
    result += (thousands === 1 ? "ein" : belowMillion(thousands)) + "tausend";
    remaining %= 1000;
    if (remaining > 0) result += " ";
  }

  if (remaining > 0) {
    result += belowMillion(remaining);
  }

  // Capitalize first letter and add "Euro"
  result = result.trim();
  result = result.charAt(0).toUpperCase() + result.slice(1);
  return result + " Euro";
}

function formatGermanPrice(amount: number): string {
  const rounded = Math.round(amount);
  if (amount === rounded) {
    return rounded.toLocaleString("de-DE") + " €";
  }
  return amount.toLocaleString("de-DE", { minimumFractionDigits: 2, maximumFractionDigits: 2 }) + " €";
}

function formatDateDE(date: Date | string | null | undefined): string {
  if (!date) return "";
  const d = typeof date === "string" ? new Date(date) : date;
  return new Intl.DateTimeFormat("de-DE", { day: "2-digit", month: "2-digit", year: "numeric" }).format(d);
}

function formatMonthYearDE(date: Date | string | null | undefined): string {
  if (!date) return "";
  const d = typeof date === "string" ? new Date(date) : date;
  const mm = String(d.getMonth() + 1).padStart(2, "0");
  const yyyy = d.getFullYear();
  return `${mm}-${yyyy}`;
}

