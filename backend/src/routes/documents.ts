// Launch browser compatible with both dev (puppeteer) and production (serverless chromium)
async function launchBrowser() {
  const isProduction = process.env.NODE_ENV === "production" || process.env.VIBECODE_PRODUCTION === "true";
  if (isProduction) {
    const executablePath = await chromium.executablePath();
    return puppeteerCore.launch({
      args: [...chromium.args, "--disable-dev-shm-usage"],
      executablePath,
      headless: true,
    });
  }
  return puppeteer.launch({
    args: ["--no-sandbox", "--disable-setuid-sandbox", "--disable-dev-shm-usage"],
    headless: true,
  });
}

async function htmlToPdf(html: string): Promise<Uint8Array> {
  const browser = await launchBrowser();
  try {
    const page = await browser.newPage();
    await page.setContent(html, { waitUntil: "networkidle0" });
    const pdfBuffer = await page.pdf({
      format: "A4",
      printBackground: true,
      margin: { top: "0", right: "0", bottom: "0", left: "0" },
    });
    console.info(`[documents] pdf_generated bytes=${pdfBuffer.length}`);
    return pdfBuffer;
  } catch (error) {
    console.error("[documents] pdf_generation_failed", error);
    throw error;
  } finally {
    await browser.close();
  }
}

