/**
 * Tax calculation:
 * - Regular taxation (marginTaxed=false): gross = net * (1 + taxRate/100)
 * - Differenzbesteuerung (marginTaxed=true): sellingPrice IS the gross price
 *   (margin tax applies to the profit margin only, not shown to customer)
 */
function calculateGross(net: number, taxRate: number, marginTaxed: boolean): number {
  if (marginTaxed) {
    return net; // Price IS gross for Differenzbesteuerung
  }
  return net * (1 + taxRate / 100);
}

function calculateTaxAmount(net: number, taxRate: number, marginTaxed: boolean): number {
  if (marginTaxed) {
    return 0; // No separate tax shown for Differenzbesteuerung
  }
  return net * (taxRate / 100);
}

